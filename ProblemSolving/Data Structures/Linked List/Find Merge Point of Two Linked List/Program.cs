    static int findMergeNode(SinglyLinkedListNode head1, SinglyLinkedListNode head2) {

        var currentHead1 = head1;

        var currentHead2 = head2;


        // Swaping heads until nodes meet.
        // Gotten from: https://stackoverflow.com/questions/1594061/check-if-two-linked-lists-merge-if-so-where/14956113#14956113
        while(currentHead1 != currentHead2)
        {
            if(currentHead1.next != null)
            {
                currentHead1 = currentHead1.next;
            }
            else
            {
                currentHead1 = head2;
            }

            if(currentHead2.next != null)
            {
                currentHead2 = currentHead2.next;
            }
            else
            {
                currentHead2 = head1;
            }
        }

        return currentHead1.data;
    }

